    private void btAvaliarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAvaliarActionPerformed
        ckInsideX.setSelected(false);
        ckInsideY.setSelected(false);
        ckPrimeiro.setSelected(false);
        ckSegundo.setSelected(false);
        ckTerceiro.setSelected(false);
        ckQuarto.setSelected(false);
        
        Ponto ponto = new Ponto(Integer.valueOf(lbCampoX.getText()), Integer.valueOf(lbCampoY.getText()));
        Quadrante quadrante = ponto.identificarQuadrante();
        
        
        switch(quadrante){
            case PRIMEIRO:
                ckPrimeiro.setSelected(true);
                break;
            case SEGUNDO:
                ckSegundo.setSelected(true);
                break;
            case TERCEIRO:
                ckTerceiro.setSelected(true);
                break;
            case QUARTO:
                ckQuarto.setSelected(true);
                break;
            default: 
                if(ponto.getX() == 0 && ponto.getY() == 0){
                    ckInsideX.setSelected(true);
                    ckInsideY.setSelected(true);
                }
                
                else if(ponto.getX() == 0){
                    ckInsideX.setSelected(true);
                }
                
                else if(ponto.getY() == 0){
                    ckInsideY.setSelected(true);
                }
        }   
    }//GEN-LAST:event_btAvaliarActionPerformed

